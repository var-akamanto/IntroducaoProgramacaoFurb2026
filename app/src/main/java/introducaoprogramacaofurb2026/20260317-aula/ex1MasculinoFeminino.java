public class ex1MasculinoFeminino {
    public static void main(String[] args) {
        char sexo = 'M';

        if (sexo == 'M') {
            System.out.println("Masculino");
        } else if (sexo == 'F') {
            System.out.println("Feminino");
        } else {
            System.out.println("Sexo inválido");
        }
    }

}
